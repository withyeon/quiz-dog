interface PixelDogProps {
  size?: number
  className?: string
  animate?: boolean
}

/* ────────── 공통:픽셀 격자를 SVG rect 배열로 변환 ────────── */
function PixelGrid({
  pixels,
  palette,
  cols,
  size,
}: {
  pixels: number[][]
  palette: Record<number, string>
  cols: number
  size: number
}) {
  const rows = pixels.length
  return (
    <svg
      width={size}
      height={Math.round((size * rows) / cols)}
      viewBox={`0 0 ${cols} ${rows}`}
      style={{ imageRendering: 'pixelated' }}
    >
      {pixels.map((row, r) =>
        row.map((v, c) =>
          v !== 0 ? (
            <rect key={`${r}-${c}`} x={c} y={r} width={1} height={1} fill={palette[v]} />
          ) : null
        )
      )}
    </svg>
  )
}

